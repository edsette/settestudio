// Current language
let currentLanguage = 'pt';

// Translation manager class
class TranslationManager {
    constructor() {
        this.currentLang = this.getStoredLanguage() || this.detectBrowserLanguage() || 'pt';
        this.init();
    }
    
    init() {
        this.setLanguage(this.currentLang);
        this.bindEvents();
    }
    
    detectBrowserLanguage() {
        const browserLang = navigator.language || navigator.userLanguage;
        const langCode = browserLang.split('-')[0];
        
        const supportedLanguages = ['pt', 'en', 'es', 'fr', 'de', 'it'];
        return supportedLanguages.includes(langCode) ? langCode : null;
    }
    
    getStoredLanguage() {
        return localStorage.getItem('preferred-language');
    }
    
    setStoredLanguage(lang) {
        localStorage.setItem('preferred-language', lang);
    }
    
    setLanguage(lang) {
        this.currentLang = lang;
        this.updateAllTranslations();
        this.updateActiveLanguageButton();
        this.setStoredLanguage(lang);
        
        // Update HTML lang attribute
        const langMap = {
            'pt': 'pt-BR',
            'en': 'en-US',
            'es': 'es-ES',
            'fr': 'fr-FR',
            'de': 'de-DE',
            'it': 'it-IT'
        };
        document.documentElement.lang = langMap[lang] || 'en-US';
        
        // Dispatch custom event
        window.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: lang } }));
    }
    
    updateAllTranslations() {
        const elements = document.querySelectorAll('[data-i18n]');
        
        elements.forEach(element => {
            const key = element.getAttribute('data-i18n');
            const translation = this.getTranslation(key);
            
            if (translation) {
                if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                    element.placeholder = translation;
                } else {
                    element.textContent = translation;
                }
            }
        });
        
        // Update placeholders
        const placeholderElements = document.querySelectorAll('[data-i18n-placeholder]');
        placeholderElements.forEach(element => {
            const key = element.getAttribute('data-i18n-placeholder');
            const translation = this.getTranslation(key);
            
            if (translation) {
                element.placeholder = translation;
            }
        });
    }
    
    getTranslation(key) {
        const langData = translations[this.currentLang];
        return langData ? langData[key] : null;
    }
    
    updateActiveLanguageButton() {
        // Update current language display
        const currentLangSpan = document.getElementById('current-lang');
        if (currentLangSpan) {
            const langLabels = {
                'pt': 'PT',
                'en': 'EN',
                'es': 'ES',
                'fr': 'FR',
                'de': 'DE',
                'it': 'IT'
            };
            currentLangSpan.textContent = langLabels[this.currentLang] || 'PT';
        }
        
        // Update active state in dropdown
        const langOptions = document.querySelectorAll('.lang-option');
        langOptions.forEach(option => {
            option.classList.remove('active');
            if (option.getAttribute('data-lang') === this.currentLang) {
                option.classList.add('active');
            }
        });
    }
    
    bindEvents() {
        // Language option clicks
        const langOptions = document.querySelectorAll('.lang-option');
        langOptions.forEach(option => {
            option.addEventListener('click', (e) => {
                const lang = e.target.getAttribute('data-lang');
                this.setLanguage(lang);
                this.toggleLanguageDropdown(false);
            });
        });
        
        // Language button toggle
        const languageBtn = document.getElementById('language-btn');
        if (languageBtn) {
            languageBtn.addEventListener('click', () => {
                this.toggleLanguageDropdown();
            });
        }
        
        // Close dropdown when clicking outside
        document.addEventListener('click', (e) => {
            const selector = document.querySelector('.language-selector');
            if (selector && !selector.contains(e.target)) {
                this.toggleLanguageDropdown(false);
            }
        });
    }
    
    toggleLanguageDropdown(show) {
        const selector = document.querySelector('.language-selector');
        if (selector) {
            if (show === false) {
                selector.classList.remove('active');
            } else if (show === true) {
                selector.classList.add('active');
            } else {
                selector.classList.toggle('active');
            }
        }
    }
    
    getCurrentLanguage() {
        return this.currentLang;
    }
}

// Initialize translation manager when DOM is ready
let translationManager;

document.addEventListener('DOMContentLoaded', () => {
    translationManager = new TranslationManager();
});

